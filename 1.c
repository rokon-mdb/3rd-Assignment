//declaring one number as maximum from two
#include<stdio.h>

int main()

{
    int x, y;
    printf("Input 1st number: ");
    scanf("%d", &x);
    printf("Input 2nd number: ");
    scanf("%d", &y);
    if(x>y)
        printf("\nMaximum number is %d", x);
    else
        printf("Maximum number is %d \n\n\n", y);






    return 0;
}
